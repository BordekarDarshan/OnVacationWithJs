// Imperative
// Tells the machine what to do and how to do it.
for (let index = 0; index < 10; index++) {
  const element = index;
  console.log(element);
}

// Declearative
// What to do and what should happen
// here we don't tell iterator to increment by one.
[1, 2, 3].map((item) => console.log(item));

// "FP" helps us be more declarative
